let currentLang = 'ko';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;

    document.documentElement.lang = lang;

    // Update text content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Use innerHTML to support <br> and <span> tags in translation
            el.innerHTML = translations[lang][key];
        }
    });

    // Update placeholders
    const inputs = document.querySelectorAll('[data-i18n-ph]');
    inputs.forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update Toggle Button State
    const btnKo = document.getElementById('lang-ko');
    const btnEn = document.getElementById('lang-en');
    const switchContainer = document.querySelector('.lang-switch'); // Select the container

    if (btnKo && btnEn && switchContainer) {
        if (lang === 'ko') {
            btnKo.classList.add('active');
            btnEn.classList.remove('active');
            switchContainer.classList.remove('en-active'); // Move slider to left
        } else {
            btnKo.classList.remove('active');
            btnEn.classList.add('active');
            switchContainer.classList.add('en-active'); // Move slider to right
        }
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const btnKo = document.getElementById('lang-ko');
    const btnEn = document.getElementById('lang-en');

    if (btnKo) {
        btnKo.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage('ko');
        });
    }

    if (btnEn) {
        btnEn.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage('en');
        });
    }
});
